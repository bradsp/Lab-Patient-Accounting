-- ============================================================================
-- schema-fixups.sql  —  Phase 22-02 Task 2  (SQL-Server-ism fix-ups)
-- ----------------------------------------------------------------------------
-- Applies AFTER Database/Postgres/schema-tables.sql (the pgloader-generated DDL).
-- Run order (clean from scratch):
--   psql -f schema-tables.sql      (pgloader output: tables, indexes, PKs, FKs)
--   psql -f schema-fixups.sql      (this file: identities, computed cols, defaults)
--
-- What pgloader did NOT translate correctly, fixed here:
--   1. IDENTITY columns      -> GENERATED BY DEFAULT AS IDENTITY (integer types) or
--                               a sequence DEFAULT nextval(...) (numeric/decimal types,
--                               which PG IDENTITY cannot use). BY DEFAULT / nextval both
--                               allow explicit inserts (needed for Phase 25 data migration).
--   2. Computed columns      -> GENERATED ALWAYS AS (expr) STORED where deterministic;
--                               plain column where the T-SQL is non-deterministic or
--                               references a not-yet-ported UDF (documented inline).
--   3. T-SQL text defaults   -> pgloader-broken string-function/concat defaults were
--                               dropped during load; meaningful literal defaults and
--                               getdate()->now() defaults are re-applied here.
--   4. xml btree index       -> pgloader's CREATE INDEX on an xml column failed (xml has
--                               no btree opclass); recreated here as a functional index.
--
-- IDENTIFIER POLICY: original SQL Server casing preserved, every identifier double-quoted
-- (pgloader `quote identifiers`). All references below quote schema, table, and column.
-- ============================================================================

-- Make the uuid generator available for any clean-from-scratch apply that runs fixups
