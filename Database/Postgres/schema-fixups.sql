-- ============================================================================
-- 1b. IDENTITY columns of NUMERIC/DECIMAL type -> sequence DEFAULT nextval(...)
--     PostgreSQL GENERATED ... AS IDENTITY requires an integer type, so the
--     numeric identity columns (e.g. chrg.chrg_num numeric(15,0)) keep their
--     numeric type for parity and get an owned sequence default. Explicit inserts
--     still work (the sequence is advanced via setval during Phase 25 migration).
-- ============================================================================
