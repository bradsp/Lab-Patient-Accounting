      ('dbo','chrg_testing_del','uri'),
      ('dbo','cli_dis','uri'),
      ('dbo','cpt4_audit','uri'),
      ('dbo','data_ErrLog','uri'),
      ('dbo','dict_quest_reference_lab_tests','uid'),
      ('dbo','phy','uri'),
      ('dbo','rds','uri'),
      ('dbo','ReChrg','urn'),
      ('dbo','temp_ssi_del','uri'),
      ('dbo','test_chk','pay_no'),
      ('dbo','test_chrg','chrg_num'),
      ('dictionary','lmrp','uid'),
      ('infce','messages_inbound','systemMsgId'),
      ('infce','messages_inbound_adt','systemMsgId'),
      ('infce','messages_inbound_webconnect','systemMsgId'),
      ('infce','messages_outbound','systemMsgId')
    ) AS t(sch, tbl, col)
  LOOP
    seqname := r.tbl || '_' || r.col || '_seq';
    -- PostgreSQL sequences must be an integer type; bigint nextval() assigns cleanly into
    -- the numeric(p,0) column. (numeric identity columns cannot use GENERATED AS IDENTITY.)
    EXECUTE format('CREATE SEQUENCE IF NOT EXISTS %I.%I AS bigint', r.sch, seqname);
    EXECUTE format('ALTER TABLE %I.%I ALTER COLUMN %I SET DEFAULT nextval(%L)',
                   r.sch, r.tbl, r.col, quote_ident(r.sch) || '.' || quote_ident(seqname));
    EXECUTE format('ALTER SEQUENCE %I.%I OWNED BY %I.%I.%I', r.sch, seqname, r.sch, r.tbl, r.col);
  END LOOP;
END $$;

