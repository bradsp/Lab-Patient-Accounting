-- 2a. Deterministic -> GENERATED ALWAYS AS (expr) STORED
--     dbo.insc.fin_class = (fin_code)
ALTER TABLE dbo."insc" DROP COLUMN IF EXISTS "fin_class";
ALTER TABLE dbo."insc"
  ADD COLUMN "fin_class" varchar GENERATED ALWAYS AS ("fin_code") STORED;

--     dbo.tempChk.total = paid + contractual + write_off
ALTER TABLE dbo."tempChk" DROP COLUMN IF EXISTS "total";
ALTER TABLE dbo."tempChk"
  ADD COLUMN "total" numeric
  GENERATED ALWAYS AS (COALESCE("paid",0) + COALESCE("contractual",0) + COALESCE("write_off",0)) STORED;

--     dbo.tempDaily.error = (startBal + charges - payments) - endBal  (isnull -> coalesce)
ALTER TABLE dbo."tempDaily" DROP COLUMN IF EXISTS "error";
ALTER TABLE dbo."tempDaily"
  ADD COLUMN "error" numeric
  GENERATED ALWAYS AS (((COALESCE("startBal",0) + COALESCE("charges",0)) - COALESCE("payments",0)) - COALESCE("endBal",0)) STORED;

-- 2b. NON-deterministic / UDF-dependent -> kept as PLAIN columns (documented).
--     These cannot be GENERATED ... STORED in PostgreSQL:
--       * dbo.chrg.age_on_date_of_service  uses datediff(...getdate()...) -> non-deterministic
--         (STORED generated expressions must be IMMUTABLE; now()/getdate() is not).
--       * dbo.chrg.calc_amt = dbo.GetAmountTotal(chrg_num) * qty  -> references a T-SQL UDF
--         not yet ported to PostgreSQL (UDF port is Phase 23).
--     pgloader already left both as plain nullable columns (int / numeric), which is the
--     desired Phase-22 fallback. The real computed logic is restored in Phase 23 (UDF port +
--     trigger or view-computed column). No action needed here; recorded for traceability.

-- ============================================================================
-- 3. Re-apply meaningful column DEFAULTS dropped during load
-- ============================================================================
-- 3a. getdate() -> now()  on the audit "mod_date"/event-date timestamp columns that are
--     semantically defaulted. (Broad set; only applied where the column exists.)
DO $$
DECLARE
  r record;
BEGIN
  FOR r IN
    SELECT n.nspname AS sch, c.relname AS tbl, a.attname AS col
    FROM pg_attribute a
    JOIN pg_class c ON a.attrelid=c.oid
    JOIN pg_namespace n ON c.relnamespace=n.oid
    WHERE c.relkind='r' AND a.attnum>0 AND NOT a.attisdropped
      AND n.nspname IN ('dbo','audit','dict','dictionary','infce','zzz','tst')
      AND a.attname IN ('mod_date','event_date','date_entered')
      AND format_type(a.atttypid,a.atttypmod) LIKE 'timestamp%'
  LOOP
    EXECUTE format('ALTER TABLE %I.%I ALTER COLUMN %I SET DEFAULT now()', r.sch, r.tbl, r.col);
  END LOOP;
END $$;

