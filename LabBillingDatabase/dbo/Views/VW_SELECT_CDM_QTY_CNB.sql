CREATE VIEW dbo.VW_SELECT_CDM_QTY_CNB
AS
SELECT     TOP (100) PERCENT cdm, SUM(qty) AS qty
FROM         dbo.chrg
WHERE     (service_date BETWEEN CONVERT(DATETIME, '2010-01-01 00:00:00', 102) AND CONVERT(DATETIME, '2010-04-30 23:59:59', 102))
GROUP BY cdm WITH ROLLUP
HAVING      (cdm IN ('5527150', '5527608', '5527515', '5525710', '5527623', '5527028', '5525512', '5527154', '5529577', '5525924', '5528712', '5525650', 
                      '5525674', '5525862', '5525652', '5525780', '5527767', '5529552', '5527794', '5529148', '5527793', '5525930', '5527366', '5527762'))
ORDER BY cdm

