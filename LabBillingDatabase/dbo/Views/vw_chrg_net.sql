/****** Object:  View dbo.vw_chrg_net    Script Date: 9/19/2001 10:47:04 AM *****
 This view created 11/27/2000 by Bradley Powers

Purpose: For use in the creation of D-Bill to net charges without showing split billing detail.

*/
CREATE VIEW dbo.vw_chrg_net
AS
SELECT     account, service_date, SUM(qty) AS qty, retail, SUM(net_amt * qty) AS net_amt, cdm
FROM         dbo.chrg
WHERE     (status NOT IN ('N/A', 'CBILL')) AND (credited = 0) AND (cdm <> 'CBILL') AND (invoice IS NULL)
GROUP BY account, service_date, cdm, retail
HAVING      (SUM(qty) <> 0)

GO
