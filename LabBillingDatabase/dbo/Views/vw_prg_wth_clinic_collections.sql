CREATE VIEW dbo.vw_prg_wth_clinic_collections
AS
SELECT     TOP (100) PERCENT dbo.acc.cl_mnem, CAST(SUM(dbo.chk.amt_paid) AS float) AS [COLLECTION AMT], SUBSTRING(CONVERT(varchar(10), 
                      dbo.chk.date_rec, 102), 6, 2) AS MONTH, DATEPART(year, dbo.chk.date_rec) AS YEAR
FROM         dbo.chk RIGHT OUTER JOIN
                      dbo.acc ON dbo.chk.account = dbo.acc.account
GROUP BY dbo.acc.cl_mnem, SUBSTRING(CONVERT(varchar(10), dbo.chk.date_rec, 102), 6, 2), DATEPART(year, dbo.chk.date_rec)
HAVING      (dbo.acc.cl_mnem IN ('ejc', 'brmc', 'cfmc', 'dwl', 'goo', 'dmg', 'dmgh', 'dmgn'))
ORDER BY dbo.acc.cl_mnem, YEAR, MONTH

